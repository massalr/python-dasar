a = 10
b = 3

# operasi tambah +
hasil = a + b
print(f"{a} + {b} = {hasil}")

# operasi kurang -
hasil = a - b
print(f"{a} - {b} = {hasil}")

# operasi perkalian *
hasil = a * b
print(f"{a} * {b} = {hasil}")

# operasi pembagian /
hasil = a / b
print(f"{a} / {b} = {hasil}")

# operasi eksponen (pangkat) **
hasil = a ** b
print(f"{a} ** {b} = {hasil}")

# operasi modulus %
hasil = a % b
print(f"{a} % {b} = {hasil}")

# operasi floor division //
hasil = a // b
print(f"{a} // {b} = {hasil}")