# perulangan

# perulangan menggunakan list
angka = [0,1,2,3,4,5]

for i in angka:
    print(f"angka perulangan --> {i}")

print("selesai \n")

# perulangan menggunakan range
angka_range = range(5)

for i in angka_range:
    print(f"angka perulangan --> {i}")

print("selesai \n")

# perulangan string

data_str = "teh manis hangat"

for huruf in data_str:
    print(huruf)