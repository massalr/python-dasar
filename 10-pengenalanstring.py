# cara membuat string
# srting adalah kumpulan karakter

data = "ini adalah string"
print(data, type(data))

# menggunakan single quote '...'
data = 'menggunakan single quote'
print(data)

# menggunakan double quote "..."
data = "menggunakan double quote"
print(data)

print("ini adalah hari jum'at")

# menggunakan tanda \
print('hari ini hari jum\'at')

# backslash
print("C:\\user\\Computer")

# tab
print("computer\terror")

# backspace
print("computer \berror")

# newline
print("baris pertama.\nbaris kedua.") # LF = line feed

# string literal atau raw
print(r"C:\new\folder")

# multiline literal string
print("""
nama : nama
kelas : kelas
""")

# multiline literal string dan RAW
print(r"""
nama : nama
kelas : kelas
website : www.computererror.com/nama
""")